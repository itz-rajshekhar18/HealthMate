import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  ScrollView,
  ActivityIndicator,
  StyleSheet,
  Platform,
} from 'react-native';
import { useLocalSearchParams } from 'expo-router';
import { getSharedReport, SharedReport } from '../../services/shareService';
import { Ionicons } from '@expo/vector-icons';

export default function SharedReportScreen() {
  const { id } = useLocalSearchParams<{ id: string }>();
  const [loading, setLoading] = useState(true);
  const [report, setReport] = useState<SharedReport | null>(null);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    loadReport();
  }, [id]);

  const loadReport = async () => {
    try {
      setLoading(true);
      setError(null);

      if (!id) {
        setError('Invalid report ID');
        return;
      }

      console.log('üì• Loading shared report:', id);
      const reportData = await getSharedReport(id);

      if (!reportData) {
        setError('Report not found or has expired');
        return;
      }

      setReport(reportData);
      console.log('‚úÖ Report loaded successfully');
    } catch (err: any) {
      console.error('‚ùå Error loading report:', err);
      setError(err.message || 'Failed to load report');
    } finally {
      setLoading(false);
    }
  };

  if (loading) {
    return (
      <View style={styles.centerContainer}>
        <ActivityIndicator size="large" color="#4F46E5" />
        <Text style={styles.loadingText}>Loading report...</Text>
      </View>
    );
  }

  if (error || !report) {
    return (
      <View style={styles.centerContainer}>
        <Ionicons name="alert-circle-outline" size={64} color="#EF4444" />
        <Text style={styles.errorTitle}>Report Not Available</Text>
        <Text style={styles.errorMessage}>
          {error || 'This report may have expired or been removed.'}
        </Text>
      </View>
    );
  }

  // For web, render the HTML content directly
  if (Platform.OS === 'web' && report.htmlContent) {
    return (
      <ScrollView style={styles.container}>
        <div dangerouslySetInnerHTML={{ __html: report.htmlContent }} />
      </ScrollView>
    );
  }

  // For mobile, show a formatted view
  return (
    <ScrollView style={styles.container}>
      <View style={styles.header}>
        <View style={styles.headerIcon}>
          <Ionicons name="heart" size={32} color="#4F46E5" />
        </View>
        <Text style={styles.headerTitle}>Vitals Health Report</Text>
        <Text style={styles.headerSubtitle}>Personal Health Summary</Text>
      </View>

      <View style={styles.content}>
        {/* Patient Info */}
        <View style={styles.card}>
          <Text style={styles.cardTitle}>Patient Information</Text>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Name:</Text>
            <Text style={styles.infoValue}>{report.userName}</Text>
          </View>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Report Date:</Text>
            <Text style={styles.infoValue}>
              {report.createdAt.toDate().toLocaleDateString()}
            </Text>
          </View>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Period:</Text>
            <Text style={styles.infoValue}>Last {report.dateRange} days</Text>
          </View>
        </View>

        {/* Vitals Preview */}
        {report.vitals && report.vitals.length > 0 && (
          <View style={styles.card}>
            <Text style={styles.cardTitle}>Recent Vitals</Text>
            {report.vitals.map((vital: any, index: number) => (
              <View key={index} style={styles.vitalRow}>
                <Text style={styles.vitalDate}>
                  {vital.timestamp?.toDate?.()?.toLocaleDateString() || 'N/A'}
                </Text>
                <View style={styles.vitalValues}>
                  <Text style={styles.vitalText}>
                    BP: {vital.bloodPressure.systolic}/{vital.bloodPressure.diastolic}
                  </Text>
                  <Text style={styles.vitalText}>HR: {vital.heartRate}</Text>
                  <Text style={styles.vitalText}>SpO‚ÇÇ: {vital.spO2}%</Text>
                </View>
              </View>
            ))}
          </View>
        )}

        {/* Disclaimer */}
        <View style={styles.disclaimer}>
          <Ionicons name="information-circle" size={20} color="#F59E0B" />
          <Text style={styles.disclaimerText}>
            This report is for informational purposes only and should not replace professional
            medical advice.
          </Text>
        </View>

        {/* Footer */}
        <View style={styles.footer}>
          <Text style={styles.footerText}>Generated by HealthMate App</Text>
          <Text style={styles.footerText}>
            Expires: {report.expiresAt.toDate().toLocaleDateString()}
          </Text>
        </View>
      </View>
    </ScrollView>
  );
}

